# This program prints the first 20 even numbers using a loop

def main():
    # Loop runs 20 times, from 0 to 19
    for i in range(20):
        even_number = i * 2
        print(even_number)

# This ensures the main function runs when the script is executed
if __name__ == '__main__':
    main()
